/**
 *  @param [in] cycle cycle对象
 *  @return void
 *  
 *  该函数在worker进程中loop部分，里面具有调用epoll_process函数
 */
void
ngx_process_events_and_timers(ngx_cycle_t *cycle)
{
    ngx_uint_t  flags;  //两个标识，一个是关于定时器的，另外一个则是关于accept post event（NGX_POST_EVENTS）的
    ngx_msec_t  timer, delta;

    //timer值最后将作为epoll_wait的超时时间(timeout)
    if (ngx_timer_resolution) {
        timer = NGX_TIMER_INFINITE;     //-1,阻塞方式等待
        flags = 0;

    } else {
        timer = ngx_event_find_timer(); //在定时器红黑树中找出最小定时时间
        flags = NGX_UPDATE_TIME;

#if (NGX_WIN32)

        /* handle signals from master in case of network inactivity */

        if (timer == NGX_TIMER_INFINITE || timer > 500) {   //win系统下，定时器大于500或者无限时设置为500
            timer = 500;
        }

#endif
    }

    /**
     * ngx_use_accept_mutex表示是否需要通过对accept加锁来解决惊群问题。
     * 当nginx worker进程数>1时且配置文件中打开accept_mutex时，这个标志置为1
     */
    if (ngx_use_accept_mutex) {
        /**
         * ngx_accept_disabled表示此时满负荷，没必要再处理新连接了，
		 * 我们在nginx.conf曾经配置了每一个nginx worker进程能够处理的最大连接数，
         * 当达到最大数的7/8时，ngx_accept_disabled为正，说明本nginx worker进程非常繁忙，将不再去处理新连接，这也是个简单的负载均衡
         */
        if (ngx_accept_disabled > 0) {
            ngx_accept_disabled--;

        } else {
            /**
             * \file ngx_event_accept.c
			 * 尝试锁ngx_accept_mutex，只有成功获取锁的进程，才会将listen套接字放入epoll中。
             * 因此，这就保证了只有一个worker进程拥有监听套接口，故所有进程阻塞在epoll_wait时，不会出现惊群现象。
             *
             * 多个worker仅有一个可以得到这把锁，获取成功的话ngx_accept_mutex_held被置为1，拿到锁，意味着监听句柄被放到本进程的epoll中了，
             * 如果没有拿到锁，则监听句柄会被从epoll中取出。
             */
            if (ngx_trylock_accept_mutex(cycle) == NGX_ERROR) {
                return;
            }

            /**
             * 拿到锁的话，置flag为NGX_POST_EVENTS
             * 这意味着当socket有数据被唤醒时，我们并不会马上accept或者说读取，而是将这个事件保存起来，然后当我们释放锁之后，才会进行accept或者读取这个句柄
			 * 
			 * ngx_process_events(epoll被设置为ngx_epoll_process_events)函数中，任何事件都将延后处理，
             * 会把accept事件都放到ngx_posted_accept_events链表中，epollin|epollout事件都放到ngx_posted_events链表中
             */
            if (ngx_accept_mutex_held) {
                flags |= NGX_POST_EVENTS;   //设置为后置处理

            } else {
                /**
                 * 这个timer实际是传给epoll_wait的超时时间。让进程起到阻塞休眠效果。
				 * 
				 * 拿不到锁，并不会马上再去获得锁，而是设置一个时间间隔，然后在epoll休眠（休眠操作由epoll_wait超时时间控制）。
				 * 此时如果有连接到达，当前休眠进程会被提前唤醒，然后立即accept。（该段时间所有连接都由此进程处理）
				 * 否则，休眠ngx_accept_mutex_delay时间，然后继续try lock
                 */
                if (timer == NGX_TIMER_INFINITE
                    || timer > ngx_accept_mutex_delay)
                {
                    timer = ngx_accept_mutex_delay;
                }
            }
        }
    }

    delta = ngx_current_msec;   //如果flags中有NGX_UPDATE_TIME，该变量会发生变化

    /**
     * 事件处理核心函数
     * 
     * #define ngx_process_events  ngx_event_actions.process_events
     * 如果调用epoll模块，有效调用为ngx_epoll_process_events函数
     */
    (void) ngx_process_events(cycle, timer, flags);

    delta = ngx_current_msec - delta;

    ngx_log_debug1(NGX_LOG_DEBUG_EVENT, cycle->log, 0,
                   "timer delta: %M", delta);

    /**
     * 先处理ngx_posted_accept_events队列中的事件，处理完毕后立即释放ngx_accept_mutex锁，
     * 接着再处理ngx_posted_events队列中事件。这样大大减少了ngx_accept_mutex锁占用的时间。
     */
    ngx_event_process_posted(cycle, &ngx_posted_accept_events);

    //释放锁后再处理下面的EPOLLIN EPOLLOUT请求
    if (ngx_accept_mutex_held) {
        ngx_shmtx_unlock(&ngx_accept_mutex);    //释放accept互斥锁
    }

    /**
     * 这里的delta其实是用来反应epoll_wait阻塞了多长时间，所以delta等于0时表示本次epoll_wait几乎没有阻塞，
     * 所以上一次的事件循环和本次事件循环是在几乎0延迟的时间内完成的，当前时间没有发生改变，故不需要去检查定时事件。
     */
    if (delta) {
        ngx_event_expire_timers();  //定时器事件。检查定时器红黑树中是否有已经超时或者是到点的定时事件，如果有，则执行事件回调函数
    }

    /**
     * 然后再处理正常的数据读写请求。
     * 因为这些请求耗时久，所以在ngx_process_events里NGX_POST_EVENTS标志将事件都放入ngx_posted_events链表中，延迟到锁释放了再处理。
     */
    ngx_event_process_posted(cycle, &ngx_posted_events);    //在进程中直接处理，回调函数
}

/**
 *
 * 添加事件到epoll事件监听队列中
 */
ngx_int_t
ngx_handle_read_event(ngx_event_t *rev, ngx_uint_t flags)
{
    if (ngx_event_flags & NGX_USE_CLEAR_EVENT) {

        /* kqueue, epoll */

        if (!rev->active && !rev->ready) {
            if (ngx_add_event(rev, NGX_READ_EVENT, NGX_CLEAR_EVENT)
                == NGX_ERROR)
            {
                return NGX_ERROR;
            }
        }

        return NGX_OK;

    } else if (ngx_event_flags & NGX_USE_LEVEL_EVENT) {

        /* select, poll, /dev/poll */

        if (!rev->active && !rev->ready) {
            if (ngx_add_event(rev, NGX_READ_EVENT, NGX_LEVEL_EVENT)
                == NGX_ERROR)
            {
                return NGX_ERROR;
            }

            return NGX_OK;
        }

        if (rev->active && (rev->ready || (flags & NGX_CLOSE_EVENT))) {
            if (ngx_del_event(rev, NGX_READ_EVENT, NGX_LEVEL_EVENT | flags)
                == NGX_ERROR)
            {
                return NGX_ERROR;
            }

            return NGX_OK;
        }

    } else if (ngx_event_flags & NGX_USE_EVENTPORT_EVENT) {

        /* event ports */

        if (!rev->active && !rev->ready) {
            if (ngx_add_event(rev, NGX_READ_EVENT, 0) == NGX_ERROR) {
                return NGX_ERROR;
            }

            return NGX_OK;
        }

        if (rev->oneshot && !rev->ready) {
            if (ngx_del_event(rev, NGX_READ_EVENT, 0) == NGX_ERROR) {
                return NGX_ERROR;
            }

            return NGX_OK;
        }
    }

    /* iocp */

    return NGX_OK;
}


ngx_int_t
ngx_handle_write_event(ngx_event_t *wev, size_t lowat)
{
    ngx_connection_t  *c;

    if (lowat) {
        c = wev->data;

        if (ngx_send_lowat(c, lowat) == NGX_ERROR) {
            return NGX_ERROR;
        }
    }

    if (ngx_event_flags & NGX_USE_CLEAR_EVENT) {

        /* kqueue, epoll */

        if (!wev->active && !wev->ready) {
            if (ngx_add_event(wev, NGX_WRITE_EVENT,
                              NGX_CLEAR_EVENT | (lowat ? NGX_LOWAT_EVENT : 0))
                == NGX_ERROR)
            {
                return NGX_ERROR;
            }
        }

        return NGX_OK;

    } else if (ngx_event_flags & NGX_USE_LEVEL_EVENT) {

        /* select, poll, /dev/poll */

        if (!wev->active && !wev->ready) {
            if (ngx_add_event(wev, NGX_WRITE_EVENT, NGX_LEVEL_EVENT)
                == NGX_ERROR)
            {
                return NGX_ERROR;
            }

            return NGX_OK;
        }

        if (wev->active && wev->ready) {
            if (ngx_del_event(wev, NGX_WRITE_EVENT, NGX_LEVEL_EVENT)
                == NGX_ERROR)
            {
                return NGX_ERROR;
            }

            return NGX_OK;
        }

    } else if (ngx_event_flags & NGX_USE_EVENTPORT_EVENT) {

        /* event ports */

        if (!wev->active && !wev->ready) {
            if (ngx_add_event(wev, NGX_WRITE_EVENT, 0) == NGX_ERROR) {
                return NGX_ERROR;
            }

            return NGX_OK;
        }

        if (wev->oneshot && wev->ready) {
            if (ngx_del_event(wev, NGX_WRITE_EVENT, 0) == NGX_ERROR) {
                return NGX_ERROR;
            }

            return NGX_OK;
        }
    }

    /* iocp */

    return NGX_OK;
}


static char *
ngx_event_init_conf(ngx_cycle_t *cycle, void *conf)
{
    if (ngx_get_conf(cycle->conf_ctx, ngx_events_module) == NULL) {
        ngx_log_error(NGX_LOG_EMERG, cycle->log, 0,
                      "no \"events\" section in configuration");
        return NGX_CONF_ERROR;
    }

    return NGX_CONF_OK;
}

/**
 *  @param [in] cycle cycle对象
 *  @return NGX_OK
 *  
 *  初始化event模块
 */
static ngx_int_t
ngx_event_module_init(ngx_cycle_t *cycle)
{
    void              ***cf;
    u_char              *shared;
    size_t               size, cl;
    ngx_shm_t            shm;
    ngx_time_t          *tp;
    ngx_core_conf_t     *ccf;
    ngx_event_conf_t    *ecf;

    cf = ngx_get_conf(cycle->conf_ctx, ngx_events_module);  //event配置
    ecf = (*cf)[ngx_event_core_module.ctx_index];   //event配置

    if (!ngx_test_config && ngx_process <= NGX_PROCESS_MASTER) {  //单进程 && 主进程
        ngx_log_error(NGX_LOG_NOTICE, cycle->log, 0,
                      "using the \"%s\" event method", ecf->name);
    }

    ccf = (ngx_core_conf_t *) ngx_get_conf(cycle->conf_ctx, ngx_core_module);

    ngx_timer_resolution = ccf->timer_resolution;   //resolution时间

#if !(NGX_WIN32)
    {
    ngx_int_t      limit;
    struct rlimit  rlmt;

    //进程可以打开的最大文件描述符数
    if (getrlimit(RLIMIT_NOFILE, &rlmt) == -1) {
        ngx_log_error(NGX_LOG_ALERT, cycle->log, ngx_errno,
                      "getrlimit(RLIMIT_NOFILE) failed, ignored");

    } else {
        if (ecf->connections > (ngx_uint_t) rlmt.rlim_cur
            && (ccf->rlimit_nofile == NGX_CONF_UNSET
                || ecf->connections > (ngx_uint_t) ccf->rlimit_nofile))
        {
            limit = (ccf->rlimit_nofile == NGX_CONF_UNSET) ?
                         (ngx_int_t) rlmt.rlim_cur : ccf->rlimit_nofile;

            ngx_log_error(NGX_LOG_WARN, cycle->log, 0,
                          "%ui worker_connections exceed "
                          "open file resource limit: %i",
                          ecf->connections, limit);
        }
    }
    }
#endif /* !(NGX_WIN32) */


    if (ccf->master == 0) {
        return NGX_OK;
    }

    if (ngx_accept_mutex_ptr) {     //共享内存锁
        return NGX_OK;
    }


    /* cl should be equal to or greater than cache line size */

    cl = 128;

    /**
     * 后面将会创建size大小的共享内存，这块共享内存将被均分成三段，
     * 分别供ngx_accept_mutex、ngx_connection_counter、ngx_temp_number使用
     */
    size = cl            /* ngx_accept_mutex */
           + cl          /* ngx_connection_counter */
           + cl;         /* ngx_temp_number */

#if (NGX_STAT_STUB)

    size += cl           /* ngx_stat_accepted */
           + cl          /* ngx_stat_handled */
           + cl          /* ngx_stat_requests */
           + cl          /* ngx_stat_active */
           + cl          /* ngx_stat_reading */
           + cl          /* ngx_stat_writing */
           + cl;         /* ngx_stat_waiting */

#endif

    shm.size = size;
    shm.name.len = sizeof("nginx_shared_zone") - 1;
    shm.name.data = (u_char *) "nginx_shared_zone";
    shm.log = cycle->log;

    /**
     *  \file ../os/unix/ngx_shmem.h|c
     *  分配共享内存
     */
    if (ngx_shm_alloc(&shm) != NGX_OK) {
        return NGX_ERROR;
    }

    shared = shm.addr;

    ngx_accept_mutex_ptr = (ngx_atomic_t *) shared; //存放互斥量内存地址的指针
    ngx_accept_mutex.spin = (ngx_uint_t) -1;        //初始化自旋锁的初值为-1

    /**
     *  \file ../../core/ngx_shmtx.h|c
     *  初始化互斥体。
     */
    if (ngx_shmtx_create(&ngx_accept_mutex, (ngx_shmtx_sh_t *) shared,
                         cycle->lock_file.data)
        != NGX_OK)
    {
        return NGX_ERROR;
    }

    //保存当前服务器总共持有的connection
    ngx_connection_counter = (ngx_atomic_t *) (shared + 1 * cl);    //使用共享内存

    (void) ngx_atomic_cmp_set(ngx_connection_counter, 0, 1);  //初始化为1

    ngx_log_debug2(NGX_LOG_DEBUG_EVENT, cycle->log, 0,
                   "counter: %p, %d",
                   ngx_connection_counter, *ngx_connection_counter);

    ngx_temp_number = (ngx_atomic_t *) (shared + 2 * cl);

    tp = ngx_timeofday();

    ngx_random_number = (tp->msec << 16) + ngx_pid;

#if (NGX_STAT_STUB)

    ngx_stat_accepted = (ngx_atomic_t *) (shared + 3 * cl);
    ngx_stat_handled = (ngx_atomic_t *) (shared + 4 * cl);
    ngx_stat_requests = (ngx_atomic_t *) (shared + 5 * cl);
    ngx_stat_active = (ngx_atomic_t *) (shared + 6 * cl);
    ngx_stat_reading = (ngx_atomic_t *) (shared + 7 * cl);
    ngx_stat_writing = (ngx_atomic_t *) (shared + 8 * cl);
    ngx_stat_waiting = (ngx_atomic_t *) (shared + 9 * cl);

#endif

    return NGX_OK;
}


